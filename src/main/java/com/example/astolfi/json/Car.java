package com.example.astolfi.json;

// JavaBean:
public class Car {
	private String model;
	private double km;

	public String getModel() {
		return model;
	}

	public void setModel(String model) {
		this.model = model;
	}

	public double getKm() {
		return km;
	}

	public void setKm(double km) {
		this.km = km;
	}

}
